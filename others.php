          <h2>Treatments</h2>
          <p>
            Take the person to a health centre immediately or
            <a href="tel:+254776388439">call emergency at +254776388439</a></p>
          <p>
            Provide them with information such as the compound taken, quantity
            and time of ingestion, age, weight and general health status of
            affected person</p>
          <p>
            Remove contaminated clothing and thoroughly wash the skin with soap
            and water.</p>
          <p>
            If possible perform pulse oximetry and give supplemental oxygen if
            indicated. Intubation and mechanical ventilation may be needed in a
            patient with severe hypoxia, respiratory distress or decreased
            consciousness.</p>
          <p>
            Avoid gastric lavage because of the risk of inhalation and hence
            pneumonitis. If very large amounts of kerosene have been ingested
            less than an hour earlier then lavage may be considered if the
            airway can be protected by expert intubation.</p>
          <p>
            Place the individual in a comfortable seated position in a
            well-ventilated space; try to avoid making them lie down</p>
          <p>
            If the individual is unconscious, make them lie on their side, to
            prevent aspiration of vomit into their lungs</p>
          <p>
            Following an ingestion of the substance, immediately give water to
            drink</p>
