                    <h2 class="def-heading">Children over 1 year old</h2>
                    <p>
                        <p>Open the child's airway by placing 1 hand on their forehead and gently tilting their head back and lifting the chin. Remove any visible obstructions from the mouth and nose.</p>
                        <p>Pinch their nose. Seal your mouth over their mouth, and blow steadily and firmly into their mouth, checking that their chest rises. Give 5 initial rescue breaths.
                        </p>
                        <p>Place the heel of 1 hand on the centre of their chest and push down by 5cm (about 2 inches), which is approximately one-third of the chest diameter. The quality (depth) of chest compressions is very important. Use 2 hands if you
