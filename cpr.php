                    <h2 class="def-heading">Hands-only CPR</h2>

                    <h3 class="instruction">To carry out a chest compression:</h3>

                    <p>
                        <p>Place the heel of your hand on the breastbone at the centre of the person's chest. Place your other hand on top of your first hand and interlock your fingers.</p>
                        <p>Position yourself with your shoulders above your hands.</p>
                        <p>Using your body weight (not just your arms), press straight down by 5 to 6cm (2 to 2.5 inches) on their chest.</p>
                        <p>Keeping your hands on their chest, release the compression and allow the chest to return to its original position. </p>
                        <p>Repeat these compressions at a rate of 100 to 120 times a minute until an ambulance arrives or you become exhausted.</p>
                    </p>

