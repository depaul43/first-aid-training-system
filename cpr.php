                    <h2 class="def-heading">CPR with rescue breaths</h2>
                    <p class="def">If you have been trained in CPR, including rescue breaths, and feel confident using your skills, you should give chest compressions with rescue breaths.
                    </p>

                    <p class="def">If you're not completely confident, attempt hands-only CPR instead.</p>

                    <p>
                        <p>Place the heel of your hand on the breastbone at the centre of the person's chest. Place your other hand on top of your first hand and interlock your fingers</p>
                        <p>After every 30 chest compressions, give 2 rescue breaths.</p>
                        <p>Tilt the casualty's head gently and lift the chin up with 2 fingers. Pinch the person's nose. Seal your mouth over their mouth, and blow steadily and firmly into their mouth for about 1 second. Check that their chest rises. Give
                            2 rescue breaths.</p>
                        <p>Continue with cycles of 30 chest compressions and 2 rescue breaths until they begin to recover or emergency help arrives.</p>
                    </p>

