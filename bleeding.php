        <h2>Ways of administering First Aid</h2>
        <h3>1. To Stop the Bleeding</h3>

          <p>Apply direct pressure on the cut or wound with a clean cloth,
            tissue, or piece of gauze until bleeding stops.</p>

          <p>If blood soaks through the material, don’t remove it. Put more cloth
            or gauze on top of it and continue to apply pressure.</p>

          <p>If the wound is on the arm or leg, raise limb above the heart, if
            possible, to help slow bleeding.</p>

          <p>Wash your hands again after giving first aid and before cleaning and
            dressing the wound. Do not apply a tourniquet unless the bleeding is
            severe and not stopped with direct pressure.</p>
    
        <h3>2. Clean Wound</h3>
        <p>
            Gently clean with soap and warm water. Try to rinse soap out of
            wound to prevent irritation. Don’t use hydrogen peroxide or iodine,
            which can damage tissue.</p>
      
        <h3>3. Protect the Wound</h3>
          <p>
            Apply antibiotic cream to reduce risk of infection and cover with a
            sterile bandage. Also try to change the bandage daily to keep the
            wound clean and dry.</p>
