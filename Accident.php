      <div class="steps">
        <p>
          <p>
            <h4>Check yourself to see if you are hurt:</h4>
            First check yourself to know if you have been injured; whether minor
            or majorly. Try to access how well you can move your limbs. Remember
            you have to be fit to help others.</p>
          <p>
            <h4>Check the other person or people for Injuries:</h4>
            If any other person(s) are injured, first access the extent of
            injury(ies). Treat the quietest person first, they are usually the
            most fatal. For people who can scream or talk, you can ask of their
            names, if he/she responds, then you know they can access the
            situation and has most likely not suffered severe head injury.</p>
          <p>
            <h4>Look for signs of breathing:</h4>
            Next, check if the person is breathing and if he has a pulse.</p>
          <p>
            <h4>Call for help:</h4>
            Immediately call for an ambulance or rush the person to a hospital.</p>
          <p>
            <h4>Check for obstructions in the personâ€™s mouth and throat:</h4>
            If you do not hear any breath sounds, check his/her mouth for any
            obstructions. If there is something obstructing the airway, use your
            index and middle finger to clear the airway.</p>
          <p>
            <h4>Perform life saving techniques:</h4>
            If there is no pulse, start <a href="cpr.php">CPR</a>. Keep the
            person s neck straight to start CPR (Cardio pulmonary
            resuscitation).You could read more about how to perform CPR
            <a href="cpr.php">here</a>.</p>
          <p>
            <h4>Ways to help him/her in grave situations:</h4>
            If there is bleeding from the mouth or the patient is vomiting, turn
            the person to his/her side. This will avoid any chances of the
            person choking. Place the person's arm that is under him/her
            straight out and the arm closest to you across his chest.</p>
          <p>
            <h4>Keep the person warm:</h4>
            Usually accident victims feel excessively cold due to shock and
            trauma. Therefore keeping them warm is essential to survival. You
            can use whatever you have to do this, such as a T-shirt, jacket,
            etc.</p>
          <p>
            <h4>Avoid feeding the person:</h4>
            Do not give the person any water, food or other fluids through the
            mouth, it could lead to the patient choking.</p></p>
