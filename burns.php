            <h3>
                Treating minor burns:
            </h3>
            <p>
                <p>Cool down the burn. After holding the burn under cool, running water, apply cool, wet compresses until the pain subsides.</p>
                <p>Remove tight items, such as rings, from the burned area. Be gentle, but move quickly before swelling starts.</p>
                <p>Avoid breaking blisters. Blisters with fluid protect the area from infection. If a blister breaks, clean the area and gently apply an antibiotic ointment.</p>
                <p>Apply a moisturizing lotion, such as one with aloe vera. After the burned area has been cooled, apply a lotion to provide relief and to keep the area from drying out.</p>
                <p>Loosely bandage the burn. Use sterile gauze. Avoid fluffy cotton that could shed and get stuck to the healing area. Also avoid putting too much pressure on the burned skin.</p>
                <p>Take an over-the-counter pain reliever if necessary. Consider acetaminophen (Tylenol), ibuprofen (Advil), or naproxen (Aleve).</p>
                </p>
