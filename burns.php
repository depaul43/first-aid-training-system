                <h2 class="major">Treating major burns Until emergency help arrives:</h2>
                <p class="major_steps">
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp; sure you and the person who’s burned are safe and out of harm’s way. Move them away from the source of the burn. If it’s an electrical burn, 
                        turn off the power source before touching them.</p>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp; Check to see if they’re breathing. If needed, start rescue breathing, <a href="cpr.php">here</a> ,if you’ve been trained.</p>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Remove restrictive items from their body, such as belts and jewelry in or near the burned areas. Burned areas typically swell quickly</li>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Cover the burned area. Use a clean cloth or bandage that’s moistened with cool, clean water.</p>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Separate fingers and toes. If hands and feet are burned, separate the fingers and toes with dry and sterile, nonadhesive bandages.</li>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Remove clothing from burned areas, but don’t try to remove clothing that’s stuck to the skin.</p>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Avoid immersing the person or burned body parts in water. Hypothermia (severe loss of body heat) can occur if you immerse large, severe burns in water.</li>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Raise the burned area. If possible, elevate the burned area above their heart.</p>
                    <p><strong style="font-size: x-large;">.</strong>&nbsp; &nbsp;Watch for shock. Signs and symptoms of shock include shallow breathing, pale complexion, and fainting.</p>
                </p>
