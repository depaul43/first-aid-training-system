            <h2>Is it a major or minor burn?</h2>
            <p class="call">
                Call the emergency number or seek immediate care for major burns, which:
            </p>
                <p>
                <p>Are deep</p>
                    <p>Cause the skin to be dry and leathery</p>
                    <p>May appear charred or have patches of white, brown or black</p>
                    <p>Are larger than 3 inches (about 8 centimeters) in diameter or cover the hands, 
                            feet, face,buttocks or a major joint.</p>
