              </div>
      
              <div class="list">
                  <h3 class="lists">"Five and Five" strategy</h3>
                  <p class="def">The American Red Cross recommends a protocol of alternating five back blows and five abdominal thrusts for conscious choking victims until the object blocking the airway is dislodged. For pregnant or too obese victims, the protocol is the same, but chest thrusts are advised instead of the abdominal ones. If the victim becomes unconscious, basic CPR is recommended. Anyway, it's convenient to make someone call to emergency medical services before it happens.
      
                      The Red Cross does not specifically refer to its choking victim protocol as the "Five and Five Technique", and it differs from the Heimlich Maneuver since it includes administering back blows to the victim, contrary to Dr. Heimlich's procedure which specifically omitted back blows and was based only on his technique.</p>
              </div>
      
              <div class="list">
                  <h3 class="lists">Anti-choking devices</h3>
                  <p class="def">Since 2015, several anti-choking devices were developed and released to the market. The design of these devices is based in a vacuum mechanical effect, with no need of batteries or electric current. They usually present an attached mask, in order to make a vacuum from the patient's nose and mouth. The current models of anti-choking devices are quite similar: a direct plunger tool (LifeVac), or a vacuum syringe (backward syringe) that keeps the tongue in place by inserting a tube in the mouth (Dechoker). Some other mechanical models are in development, such as Lifewand, which makes the vacuum by direct pressure against patient's face using a button. According to a 2020 systematic review of the effectiveness of these three devices, "There are many weaknesses in the available data and few unbiased trials that test the effectiveness of anti-choking suction devices resulting in insufficient evidence to support or discourage their use. Practitioners should continue to adhere to guidelines authored by local resuscitation authorities which align with ILCOR recommendations.</p>
