                <h2 class="def-heading">Choking:</h2>
      
                <p class="def">Choking occurs when breathing is impeded by a constricted or obstructed throat or windpipe. In some cases the air flow is completely blocked, in other cases some amount of air can pass to the lungs. Insufficient oxygen being delivered to the body will result in oxygen deprivation. Although oxygen stored in the blood and lungs can keep a person alive for several minutes after breathing stops, this sequence of events can lead to death. </p>
            </div>
      
            <div class="definition">
              <h2 class="def-heading">Cause:</h2>
      
              
              <p class="def">Choking is often caused by an object from outside the body, also called a foreign body, blocking the airway. The object can block the upper or lower airway passages. The airway obstruction is usually partial, but can also be complete</p>
          </div>
          
          <div class="definition">
              <h2 class="def-heading">First Aid:</h2>
              
              <div class="list">
                  <h3 class="lists">Cough</h3>
                  <p class="def">If the choking individual is conscious and coughing, the American Red Cross recommends encouraging the individual to stay calm and continue coughing freely, and it may be easier to take air through the nose to refill the lungs. If the person choking is unable to cough or if coughing is not effective, the American Red Cross recommends to move onto other methods, detailed below</p>
              </div>
      
              <div class="list">
                  <h3 class="lists">Chest thrusts</h3>
                  <p class="def">When abdominal thrusts can not be performed on the victim (in case of pregnant or too obese victims, and others) chest thrusts are advised instead.
      
                      Chest thrusts are performed with the rescuer standing behind the choking victim. The rescuer closes his skillful hand and grasp it with the other hand. This can produce several kinds of fists, but any of them can be valid if it can be placed on the chest of the victim without sinking there a knuckle too painfully. Keeping the fist with both hands, the rescuer uses it to press forcefully inwards on the lower half of the chest bone (approximately), but not in the very endpoint (which is the xiphoid process). It can be noted, as a general reference, that, when the victim is a woman, the zone of pressure of the chest thrusts would be normally upper than the level of the breasts. The pressure is not focused against the xiphoid process, to avoid breaking it. If the first thrust doesn't solve the choking, it can be repeated several times.</p>
